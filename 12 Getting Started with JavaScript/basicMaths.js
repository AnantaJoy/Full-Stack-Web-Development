// declare variables
var num1 = 43;
var num2 = 22;
// sum of two numbers
var totalSum = num1 + num2;
console.log(totalSum);

//subtraction of two sumbers

var totalSub = num1 - num2;
console.log(totalSub);

// multiplication of two numbers

var totalMulti = num1 * num2;
console.log(totalMulti);

// division of two numbers
var totalDivision = num1 / num2;
console.log(totalDivision.toFixed(3));

// absolute value
var value1 = -16.4;
var  absoluteValue = Math.abs(value1);
console.log(absoluteValue);

// around  a value
var value2 = 18.5
var roundValue = Math.round(value2);
console.log(roundValue);

// floor a value
var floorValue = Math.floor(value2);
console.log(floorValue);

// ceil a value
console.log(Math.ceil(3.1));

