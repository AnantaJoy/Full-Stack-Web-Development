var num1 = Math.floor(Math.random()*6+1);

switch(num1){
    case 1:
        console.log('ONE')
        break;
    case 2:
        console.log('TWO')
        break;
    case 3:
        console.log('THREE')
        break;
    case 4:
        console.log('FOUR')
        break;
    case 5:
        console.log('FIVE')
        break;
    default:
        console.log('SIX')
                                        
}